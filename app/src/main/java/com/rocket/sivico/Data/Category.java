public class Category {
    private String id;
    private String name;
    private String color;
    private String icon;
    private String description;

    public Category() {
    }

    public String getDescription() {
        return description;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }

    public String getIcon() {
        return icon;
    }
}
